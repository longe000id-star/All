# 长方形面积
def rectangle_area(length, width):
    rectangle_area = length * width
    return rectangle_area

# 长方形的周长
def rectangle_perimeter(length, width):
    rectangle_perimeter = (length + width) * 2
    return rectangle_perimeter

# 正方形面积
def square_area(side):
    square_area = side ** 2
    return square_area

